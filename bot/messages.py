def welcome_message(first_name: str | None) -> str:
    name = first_name or "друг"
    return (
        f"🦁 Добро пожаловать в Московский зоопарк, {name}!\n\n"
        "Викторина «Какое у вас тотемное животное?» поможет узнать, "
        "кто из обитателей зоопарка ближе всего вашему характеру.\n\n"
        f"Ответьте на {get_question_count()} вопросов — и мы подберём вашего тотемного зверя. "
        "А заодно расскажем о программе «Возьми животное под опеку».\n\n"
        "Нажмите «Начать викторину» или отправьте /start 🎯"
    )


